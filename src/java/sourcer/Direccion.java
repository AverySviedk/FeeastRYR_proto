package sourcer;

public class Direccion {
    private String id;
    private String direccion;

    public Direccion(String id, String direccion) {
        this.id = id;
        this.direccion = direccion;
    }

    public String getId() {
        return id;
    }

    public String getDireccion() {
        return direccion;
    }
    
}
